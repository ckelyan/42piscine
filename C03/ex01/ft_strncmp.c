int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	if (n == 0)
		return (0);
	i = 1;
	while (*s1 == *s2 && i++ < n)
		if (*s1++ == '\0' || *s2++ == '\0')
			break ;
	return (*s1 - *s2);
}
