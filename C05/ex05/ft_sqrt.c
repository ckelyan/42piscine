int	ft_sqrt(int n)
{
	int	i;

	if (n < 0 || n == 2)
		return (0);
	if (n < 2)
		return (n);
	i = n / 4 + 3;
	while (--i > 0)
		if (i * i == n)
			return (i);
	return (0);
}
