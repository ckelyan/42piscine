int	ft_is_prime(int n)
{
	int	i;

	if (n == 2)
		return (1);
	if (n < 2 || n % 2 == 0)
		return (0);
	i = n;
	while (--i > 1)
		if (n % i == 0)
			return (0);
	return (1);
}
